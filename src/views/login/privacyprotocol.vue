      </p>
      <h3 data-v-21ef6bb6>A. Cookie（曲奇档案）</h3>
      <p data-v-21ef6bb6>Cookie 是您浏览分期乐平台时，服务器放在客户端（您的移动电话或其他智能终端内）里面的一个小小的文本文件，当您再次访问相同应用程序时，我们就可通过 Cookie 识别您的应用程序。Cookie 可能会存储用户偏好及其他信息。</p>
      <h3 data-v-21ef6bb6>B. Web beacon（网络信标）</h3>
      <p data-v-21ef6bb6>Web beacon 是装嵌在网站或电邮内的电子图像文件案或其他技术，可用于计算访客数目、记录您是否及何时阅览电邮或网站，或用以使用某些 Cookie。</p>
