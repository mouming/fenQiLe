      </p>
      <p data-v-21ef6bb6>
        <strong data-v-21ef6bb6>（2）个人敏感信息：</strong>个人敏感信息是指一旦泄露、非法提供或滥用可能危害人身和财产安全，极易导致个人名誉、身心健康受到损害或歧视性待遇等的个人信息。例如，个人敏感信息包括身份证件号码、个人生物识别信息、银行账号、通信内容、健康生理信息等。
      </p>
      <p data-v-21ef6bb6>
        <strong data-v-21ef6bb6>（3）关联公司：</strong>指一方现在或将来控制、受控制或与其处于共同控制下的任何公司、机构。“控制”是指直接或间接地拥有影响所提及公司管理的能力，无论是通过所有权、有投票权的股份、合同或其他被依法认定的方式。
      </p>
      <p data-v-21ef6bb6>
        <strong data-v-21ef6bb6>（4）设备</strong>：设备是指可用于访问分期乐产品和/或服务的装置，例如平板电脑或智能手机。
      </p>
      <p data-v-21ef6bb6>
        <strong data-v-21ef6bb6>（5）唯一设备标识符：</strong>唯一设备标识符（专属 ID 或 UUID）是指由设备制造商编入到设备中的一串字符，可用于以独有方式标识相应设备（例如手机的 IMEI 号）。唯一设备标识符有多种用途，其中可在不能使用 Cookie（例如在移动平台程序中）时用以提供广告。
      </p>
      <p data-v-21ef6bb6>
        <strong data-v-21ef6bb6>（6） IP地址：</strong>每台上网的设备都会指定一个编号，称为互联网协议 ( IP ) 地址。这些编号通常都是根据地理区域指定的。IP 地址通常可用于识别设备连接至互联网时所在的位置。
      </p>
      <p data-v-21ef6bb6>
        <strong data-v-21ef6bb6>（7）SSL：</strong>SSL（Secure Socket Layer）又称为安全套接层，是在传输通信协议（TCP/IP）上实现的一种安全协议。SSL 支持各种类型的网络，同时提供三种基本的安全服务，均通过使用公开密钥和对称密钥技术以达到信息保密的效果。
